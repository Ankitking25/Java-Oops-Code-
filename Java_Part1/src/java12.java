//Nested if else statement in java
class java12
{
	public static void main(String args[])
	{
		if (5<6)
		{
			if (4<7)
			{
				System.out.println("Nested loop");
			}
			else
			{
				System.out.println("nest else part");
			}
		}
		else
		{
			System.out.println("Else part of the firt if ");
		}
	}
}
