/*Switch statement in java*/
//Use with break statement:
class java14
{
	public static void main(String args[])
	{
		/*switch case statement*/
		switch(3)
		{
		case 1 :
		{
			System.out.println("Monday");
		}
		case 2 :
		{
			System.out.println("Game of throne");
		}
		case 3:
		{
			System.out.println("total 10 episode");
		}
		}
	}
}
